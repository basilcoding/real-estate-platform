import Listing from '../models/Listing.js';
import { deleteFileFromS3 } from '../utils/s3.util.js'; // Importing the utility

export const getListings = async (req, res) => {
  try {
    const listings = await Listing.find().sort({ createdAt: -1 }); // Newest first
    res.status(200).json({
      success: true,
      count: listings.length,
      listings
    });
  } catch (error) {
    res.status(500).json({ success: false, message: 'Failed to fetch listings' });
  }
};

export const createListing = async (req, res) => {
  try {
    const { title, description, price, isNegotiable, propertyType, status, address } = req.body;

    // 👇 Extract image URLs generated by S3
    const imageUrls = req.files ? req.files.map(file => file.location) : [];

    const listing = await Listing.create({
      title, description, price, isNegotiable, propertyType, status, address,
      images: imageUrls // 👈 Save URLs
    });

    res.status(201).json({ success: true, listing });
  } catch (error) {
    res.status(400).json({ success: false, message: error.message });
  }
};

export const updateListing = async (req, res) => {
  try {
    const { id } = req.params;
    const { title, description, price, isNegotiable, propertyType, status, address, existingImages } = req.body;

    // 1. Find the old listing BEFORE updating it so we know what images it used to have
    const oldListing = await Listing.findById(id);
    if (!oldListing) {
      return res.status(404).json({ success: false, message: 'Listing not found' });
    }

    // 2. Parse existing images we want to KEEP
    let imagesToKeep = [];
    if (existingImages) {
      imagesToKeep = Array.isArray(existingImages) ? existingImages : [existingImages];
    }

    // 3. Find images that were in the DB, but are NO LONGER in the imagesToKeep array
    const imagesToDelete = oldListing.images.filter(img => !imagesToKeep.includes(img));

    // 4. Delete those removed images from S3
    if (imagesToDelete.length > 0) {
      // Run all delete operations in parallel for speed
      await Promise.all(imagesToDelete.map(imgUrl => deleteFileFromS3(imgUrl)));
    }

    // 5. Add newly uploaded images and save
    const newImageUrls = req.files ? req.files.map(file => file.location) : [];
    const finalImages = [...imagesToKeep, ...newImageUrls];

    const listing = await Listing.findByIdAndUpdate(
      id,
      { title, description, price, isNegotiable, propertyType, status, address, images: finalImages },
      { returnDocument: 'after', runValidators: true }
    );

    res.status(200).json({ success: true, message: 'Listing updated successfully', listing });
  } catch (error) {
    res.status(400).json({ success: false, message: error.message || 'Failed to update listing' });
  }
};

export const deleteListing = async (req, res) => {
  try {
    const { id } = req.params;
    
    // 1. Find the listing first (don't delete it yet!)
    const listing = await Listing.findById(id);

    if (!listing) {
      return res.status(404).json({ success: false, message: 'Listing not found' });
    }

    // 2. Delete all attached images from S3
    if (listing.images && listing.images.length > 0) {
      await Promise.all(listing.images.map(imgUrl => deleteFileFromS3(imgUrl)));
    }

    // 3. Now delete the actual database record
    await Listing.findByIdAndDelete(id);

    res.status(200).json({
      success: true,
      message: 'Listing and associated images deleted successfully'
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: error.message || 'Failed to delete listing'
    });
  }
};

export const getListing = async (req, res) => {
  try {
    const { id } = req.params;
    const listing = await Listing.findById(id);

    if (!listing) {
      return res.status(404).json({ success: false, message: 'Listing not found' });
    }

    // Increment views
    listing.views += 1;
    await listing.save();

    res.status(200).json({
      success: true,
      listing
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: 'Failed to fetch listing'
    });
  }
};